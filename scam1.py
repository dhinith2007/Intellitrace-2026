# Function to extract numeric amount from transaction narration
def extract_amount(text):
    """Extract numeric amount from transaction narration"""
    if pd.isna(text):
        return np.nan

    # Pattern to find INR followed by numbers
    pattern = r'INR\s*(\d+)'
    match = re.search(pattern, str(text))

    if match:
        return float(match.group(1))
    return np.nan

