def create_synthetic_labels(df):
    """
    Create synthetic fraud labels based on patterns
    This is for demonstration - in production, you'd use actual labeled data
    """
    # Initialize all as normal (0)
    labels = np.zeros(len(df))

    # Group by mobile number to detect multi-account patterns
    mobile_groups = df.groupby('mobile_number').size()
    suspicious_mobiles = mobile_groups[mobile_groups > 1].index

    # Group by account to detect multiple transactions
    account_groups = df.groupby('account_number').size()
    high_volume_accounts = account_groups[account_groups > 3].index

    # Mark as suspicious based on patterns
    for i, row in df.iterrows():
        score = 0

        # High amount
        if row['is_high_amount']:
            score += 1

        # Cash-out transaction
        if row['is_cash_out']:
            score += 2

        # Suspicious merchant
        if row['is_suspicious_merchant']:
            score += 1

        # Multiple accounts per mobile
        if row['mobile_number'] in suspicious_mobiles:
            score += 2

        # High volume account
        if row['account_number'] in high_volume_accounts:
            score += 2

        # Mark as suspicious (1) if score >= 3
        if score >= 3:
            labels[i] = 1

    return labels

