def generate_transaction_trace(model, X_test, y_test, test_indices, feature_names):
    """
    Generate explainable transaction traces for flagged transactions
    """
    traces = []

    # Convert to DataFrame if it's a numpy array
    if isinstance(X_test, np.ndarray):
        X_test_df = pd.DataFrame(X_test, columns=feature_names)
    else:
        X_test_df = X_test.copy()

    # Handle y_test if it's a numpy array
    if isinstance(y_test, np.ndarray):
        y_test_series = pd.Series(y_test)
    else:
        y_test_series = y_test

    for idx in test_indices[:5]:  # Show first 5 examples
        if idx >= len(X_test_df):
            print(f"⚠️ Index {idx} out of range, skipping...")
            continue

        transaction = X_test_df.iloc[[idx]]
        actual = y_test_series.iloc[idx] if hasattr(y_test_series, 'iloc') else y_test_series[idx]

        # Get prediction
        proba = model.predict_proba(transaction)[0][1]
        pred = 1 if proba >= 0.5 else 0

        # Create trace
        trace = {
            'Transaction_ID': idx,
            'Risk_Score': proba * 100,
            'Predicted': 'FRAUD' if pred == 1 else 'NORMAL',
            'Actual': 'FRAUD' if actual == 1 else 'NORMAL',
            'Confidence': 'HIGH' if proba > 0.8 else 'MEDIUM' if proba > 0.5 else 'LOW',
            'Top_Risk_Factors': []
        }

        # Identify top risk factors (features with highest impact)
        # Get the top 3 feature values for this transaction
        feature_values = transaction.iloc[0]

        # Sort features by absolute value to find most influential
        influential_features = feature_values.abs().sort_values(ascending=False).head(5).index

        for feat in influential_features:
            if abs(feature_values[feat]) > 0.1:  # Only show non-trivial values
                trace['Top_Risk_Factors'].append(f"{feat}: {feature_values[feat]:.3f}")

        traces.append(trace)

    return traces

