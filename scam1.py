def calculate_risk_score(model, features, scaler, selector, feature_names):
    """
    Calculate risk scores based on model predictions
    Returns risk score (0-100) and confidence level
    """
    # Scale features
    features_scaled = scaler.transform(features)
    features_scaled = pd.DataFrame(features_scaled, columns=feature_names)

    # Select features
    features_selected = selector.transform(features_scaled)

    # Get prediction probability
    proba = model.predict_proba(features_selected)[:, 1]

    # Convert to risk score (0-100)
    risk_score = proba * 100

    # Determine confidence level
    confidence = np.where(
        risk_score > 80, 'HIGH',
        np.where(risk_score > 50, 'MEDIUM', 'LOW')
    )

    return risk_score, confidence

