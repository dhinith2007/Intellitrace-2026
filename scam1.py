# Create fraud indicators
def create_fraud_indicators(df):
    """Create multiple fraud indicators based on patterns"""

    # 1. High amount indicator (top 5% transactions)
    amount_threshold = df['amount'].quantile(0.95)
    df['is_high_amount'] = (df['amount'] > amount_threshold).astype(int)

    # 2. Cash-out transactions (ATM withdrawals)
    df['is_cash_out'] = df['transaction_type'].str.contains('ATM|WITHDRAWAL', case=False, na=False).astype(int)

    # 3. Suspicious merchants
    df['is_suspicious_merchant'] = df['merchant'].str.contains('ATM|CASH|WITHDRAWAL', case=False, na=False).astype(int)

    # 4. Round amount indicator (common in mule transactions)
    df['is_round_amount'] = (df['amount'] % 1000 == 0).astype(int)

    # 5. Odd amount pattern (like 10675, 16779 - not round)
    df['is_odd_amount'] = ((df['amount'] % 1000 != 0) & (df['amount'] % 500 != 0)).astype(int)

    return df

