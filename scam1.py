def simulate_alert_system(model, X_test, y_test, threshold=0.5):
    """
    Simulate real-time alert generation
    """
    # Get predictions
    proba = model.predict_proba(X_test)[:, 1]
    predictions = (proba >= threshold).astype(int)

    # Create alerts dataframe
    alerts = pd.DataFrame({
        'Transaction_ID': range(len(X_test)),
        'Risk_Score': proba * 100,
        'Prediction': predictions,
        'Actual': y_test.values if hasattr(y_test, 'values') else y_test,
        'Alert_Level': pd.cut(proba * 100, bins=[0, 30, 70, 100],
                              labels=['Low', 'Medium', 'High'])
    })

    # Filter high-risk alerts
    high_risk_alerts = alerts[alerts['Alert_Level'] == 'High']

    print("="*60)
    print("REAL-TIME ALERT SYSTEM SIMULATION")
    print("="*60)
    print(f"Total Transactions: {len(alerts)}")
    print(f"Alerts Generated: {len(alerts[alerts['Prediction']==1])}")
    print(f"High Risk Alerts: {len(high_risk_alerts)}")
    print(f"False Alerts: {len(alerts[(alerts['Prediction']==1) & (alerts['Actual']==0)])}")
    print(f"Missed Frauds: {len(alerts[(alerts['Prediction']==0) & (alerts['Actual']==1)])}")

    print("\n" + "-"*60)
    print("SAMPLE HIGH RISK ALERTS")
    print("-"*60)
    if len(high_risk_alerts) > 0:
        print(high_risk_alerts.head(10).to_string())
    else:
        print("No high risk alerts in this sample")

    return alerts

