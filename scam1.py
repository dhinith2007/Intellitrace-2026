# Extract transaction type and merchant from narration
def extract_transaction_details(text):
    """Extract transaction type and merchant from narration"""
    text = str(text)

    # Extract transaction type (first word)
    transaction_type = text.split()[0] if text else 'UNKNOWN'

    # Extract merchant (between 'at' and 'INR')
    merchant_pattern = r'at\s+([A-Za-z\s]+?)\s+INR'
    merchant_match = re.search(merchant_pattern, text)
    merchant = merchant_match.group(1).strip() if merchant_match else 'UNKNOWN'

    return transaction_type, merchant

